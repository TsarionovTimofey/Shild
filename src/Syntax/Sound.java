package Syntax;

public class Sound {
    public static void main(String[] args) {
        double dist;
        dist = 1100 * 7.2;
        System.out.println("Расстояние до места вспышки молнии " + "составляет "
                + dist + " футов");
        dist = 1100 * 7.2/2;
        System.out.println("Расстояние до скалы " + "составляет "
                + dist + " футов");
    }
}
